// Helper function to generate mock project counts for clients
export function getClientProjects(clientId: string): { active: number; onHold: number; completed: number } {
  // Generate deterministic but varied project counts based on client id
  const hash = clientId.split("").reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return {
    active: hash % 4,
    onHold: (hash + 1) % 3,
    completed: (hash + 2) % 3,
  };
}

